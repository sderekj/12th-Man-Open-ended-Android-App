package nyc.c4q.dereksantos.homework_unit3_week2;

public class LeagueDescription {

    private String mName;
    private Integer mLogo;

    public LeagueDescription (String mName, Integer mLogo) {
        this.mName = mName;
        this.mLogo = mLogo;
    }

    public String getmName() {
        return mName;
    }

    public Integer getmLogo() {
        return mLogo;
    }
}
